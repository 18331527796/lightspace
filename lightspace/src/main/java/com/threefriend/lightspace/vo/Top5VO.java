package com.threefriend.lightspace.vo;

public class Top5VO {

	private String name;
	
	private Integer bad;
	
	private String percentage;
	
	private Integer percentageSort;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getBad() {
		return bad;
	}

	public void setBad(Integer bad) {
		this.bad = bad;
	}

	public String getPercentage() {
		return percentage;
	}

	public void setPercentage(String percentage) {
		this.percentage = percentage;
	}

	public Integer getPercentageSort() {
		return percentageSort;
	}

	public void setPercentageSort(Integer percentageSort) {
		this.percentageSort = percentageSort;
	}
	
	
}
