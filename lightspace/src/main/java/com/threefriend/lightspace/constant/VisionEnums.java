package com.threefriend.lightspace.constant;

/**
 * 
 * 视力的程度判断
 */
public enum VisionEnums {
	NORMAL(0.81,"正常"),
	MILD(0.8,"轻度MAX"),
	MILD1(0.79,"轻度MIN"),
	MODERATE(0.4,"中度"),
	SERIOUS(0.39,"重度"),
	SERIOUS1(0.1,"重度"),
	;
	
	private Double type;
	
	private String msg;
	
	VisionEnums(Double type,String msg){
		this.type = type;
		this.msg = msg;
	}

	public Double getType() {
		return type;
	}

	public String getMsg() {
		return msg;
	}
	
	
}
